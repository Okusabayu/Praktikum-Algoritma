#include <stdio.h>

void main()
{
    int a=1; //True
    int b=0; //False

    printf("a = %d\n", a);
    printf("b = %d\n\n", b);

    //Logika AND
    printf("a && b = %d\n", a && b);

    //Logika OR
    printf("a || b = %d\n", a || b);

    //Logika NOT
    printf("!a     = %d\n", !a);
}
